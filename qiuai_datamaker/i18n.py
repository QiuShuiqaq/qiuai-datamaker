@dataclass
class I18n:
    language: str = "zh_CN"

    def set_language(self, language: str) -> None:
        if language not in SUPPORTED_LANGUAGES:
            return
        self.language = language

    def toggle_language(self) -> str:
        self.language = "en_US" if self.language == "zh_CN" else "zh_CN"
        return self.language

    def t(self, key: str, **kwargs) -> str:
        text = TRANSLATIONS.get(self.language, {}).get(key)
        if text is None:
            text = TRANSLATIONS["en_US"].get(key, key)
        if kwargs:
            return text.format(**kwargs)
        return text

    def scene_label(self, scene_key: str) -> str:
        return self.t(f"scene.{scene_key}")
